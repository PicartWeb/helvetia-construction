  modal.querySelector("[data-modal-title]").textContent = data.title;
  modal.querySelector("[data-modal-location]").textContent = data.location;
  modal.querySelector("[data-modal-category]").textContent = data.category;
  modal.querySelector("[data-modal-year]").textContent = data.year;
  modal.querySelector("[data-modal-challenge]").textContent = data.challenge;
  modal.querySelector("[data-modal-solution]").textContent = data.solution;
  modal.querySelector("[data-modal-result]").textContent = data.result;
  modal.querySelector("[data-modal-services]").innerHTML = data.services.map((service) => `<span>${service}</span>`).join("");
