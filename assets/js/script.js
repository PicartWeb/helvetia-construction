const motionAllowed = !window.matchMedia("(prefers-reduced-motion: reduce)").matches;
const parallaxNodes = motionAllowed ? document.querySelectorAll("[data-parallax-speed]") : [];
function updateParallax() {
  parallaxNodes.forEach((node) => {
    const speed = Number(node.dataset.parallaxSpeed || 0);
    const rect = node.getBoundingClientRect();
    const offset = (window.innerHeight / 2 - rect.top - rect.height / 2) * speed;
    const horizontal = (window.innerWidth / 2 - rect.left - rect.width / 2) * speed * .35;
    node.style.setProperty("--parallax-y", `${offset.toFixed(2)}px`);
    node.style.setProperty("--parallax-x", `${horizontal.toFixed(2)}px`);
  });
}
if (parallaxNodes.length) {
  updateParallax();
  window.addEventListener("scroll", updateParallax, { passive: true });
  window.addEventListener("resize", updateParallax);
}

